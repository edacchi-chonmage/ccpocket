import { beforeEach, describe, expect, it, vi } from "vitest";
import { join, resolve } from "node:path";

const { execFileSyncMock, getStagedDiffMock } = vi.hoisted(() => ({
  execFileSyncMock: vi.fn(),
  getStagedDiffMock: vi.fn(),
}));

vi.mock("node:child_process", () => ({
  execFileSync: execFileSyncMock,
}));

const {
  mkdtempSyncMock,
  readFileSyncMock,
  rmSyncMock,
} = vi.hoisted(() => ({
  mkdtempSyncMock: vi.fn(),
  readFileSyncMock: vi.fn(),
  rmSyncMock: vi.fn(),
}));

vi.mock("node:fs", () => ({
  mkdtempSync: mkdtempSyncMock,
  readFileSync: readFileSyncMock,
  rmSync: rmSyncMock,
}));

vi.mock("./git-operations.js", () => ({
  getStagedDiff: getStagedDiffMock,
}));

import { generateCommitMessage } from "./git-assist.js";

describe("generateCommitMessage", () => {
  beforeEach(() => {
    execFileSyncMock.mockReset();
    getStagedDiffMock.mockReset();
    mkdtempSyncMock.mockReset();
    readFileSyncMock.mockReset();
    rmSyncMock.mockReset();
    getStagedDiffMock.mockReturnValue("diff --git a/a.txt b/a.txt\n");
    mkdtempSyncMock.mockReturnValue("/tmp/codex-output-dir");
  });

  it("uses claude CLI for claude sessions", () => {
    execFileSyncMock.mockReturnValue("feat: generated message\n");

    const message = generateCommitMessage({
      provider: "claude",
      projectPath: "/tmp/project",
      model: "claude-sonnet-4-6",
    });

    expect(message).toBe("feat: generated message");
    expect(execFileSyncMock).toHaveBeenCalledWith(
      "claude",
      [
        "-p",
        "--model",
        "claude-sonnet-4-6",
        "Write a single Conventional Commits message in English for the staged changes below. Output only the commit message, with no quotes or explanation.",
      ],
      expect.objectContaining({
        cwd: resolve("/tmp/project"),
        input: "diff --git a/a.txt b/a.txt",
        encoding: "utf-8",
        maxBuffer: 1024 * 1024,
      }),
    );
  });

  it("uses codex CLI for codex sessions", () => {
    readFileSyncMock.mockReturnValue("fix: generated by codex\n");

    const message = generateCommitMessage({
      provider: "codex",
      projectPath: "/tmp/project",
      model: "gpt-5.4",
    });

    expect(message).toBe("fix: generated by codex");
    expect(execFileSyncMock).toHaveBeenCalledWith(
      "codex",
      [
        "exec",
        "-m",
        "gpt-5.4-mini",
        "-o",
        join("/tmp/codex-output-dir", "last-message.txt"),
        "-",
      ],
      expect.objectContaining({
        cwd: resolve("/tmp/project"),
        input:
          "Write a single Conventional Commits message in English for the staged changes below. Output only the commit message, with no quotes or explanation.\n\ndiff --git a/a.txt b/a.txt",
        encoding: "utf-8",
        maxBuffer: 1024 * 1024,
      }),
    );
    expect(readFileSyncMock).toHaveBeenCalledWith(
      "/tmp/codex-output-dir/last-message.txt",
      "utf-8",
    );
    expect(rmSyncMock).toHaveBeenCalledWith("/tmp/codex-output-dir", {
      recursive: true,
      force: true,
    });
  });

  it("ignores session codex model and uses mini for commit assist", () => {
    readFileSyncMock.mockReturnValue("chore: generated by codex mini\n");

    const message = generateCommitMessage({
      provider: "codex",
      projectPath: "/tmp/project",
      model: "gpt-5.4",
    });

    expect(message).toBe("chore: generated by codex mini");
    expect(execFileSyncMock).toHaveBeenCalledWith(
      "codex",
      expect.arrayContaining(["exec", "-m", "gpt-5.4-mini"]),
      expect.any(Object),
    );
  });

  it("fails when no files are staged", () => {
    getStagedDiffMock.mockReturnValue("");

    expect(() =>
      generateCommitMessage({
        provider: "claude",
        projectPath: "/tmp/project",
      }),
    ).toThrow(/Nothing to commit/);
  });

  it("fails when CLI returns empty output", () => {
    execFileSyncMock.mockReturnValue(" \n");

    expect(() =>
      generateCommitMessage({
        provider: "claude",
        projectPath: "/tmp/project",
      }),
    ).toThrow(/empty output/);
  });
});
